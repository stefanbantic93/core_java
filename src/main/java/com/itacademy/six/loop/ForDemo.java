package com.academy.six.loop;

/**
 * varijabla petlje => number
 * 1. uslov petlje
 * 2. blok koda petlje
 * 3. korak petlje
 */
public class ForDemo {
    public static void main(String[] args) {

        for(int number = 11; number>0; number--){
            System.out.println(number);
        }
    }
}
